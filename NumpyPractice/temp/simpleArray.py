import numpy as np

# array
x = np.array([1,2,3])
print(type(x), x)

# accessing element of the array
print(x[0]) #first element
print(x[-1]) #last element

# length
print(len(x))

#array operation 
a1 = np.array([1,2,3,4,5])
a2 = np.array([1,2,3,4,5])

print(a1+a2) #sum
print(a1-a2) #sub
print(a1*a2) #mult
print(a1/a2) #div

